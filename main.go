package main

import "fmt"

func main() {
	// Declaring variables
	var num1 int = 0 * 9
	var num2 int = 1 * 9
	var num3 int = 2 * 9
	var num4 int = 3 * 9
	var num5 int = 4 * 9
	var num6 int = 5 * 9
	var num7 int = 6 * 9
	var num8 int = 7 * 9
	var num9 int = 8 * 9
	var num10 int = 9 * 9
	var num11 int = 10 * 9
	var num12 int = 11 * 9
	var num13 int = 12 * 9

	// Printing
	fmt.Println("0 * 9 = " + fmt.Sprint(num1))
	fmt.Println("1 * 9 = " + fmt.Sprint(num2))
	fmt.Println("2 * 9 = " + fmt.Sprint(num3))
	fmt.Println("3 * 9 = " + fmt.Sprint(num4))
	fmt.Println("4 * 9 = " + fmt.Sprint(num5))
	fmt.Println("5 * 9 = " + fmt.Sprint(num6))
	fmt.Println("6 * 9 = " + fmt.Sprint(num7))
	fmt.Println("7 * 9 = " + fmt.Sprint(num8))
	fmt.Println("8 * 9 = " + fmt.Sprint(num9))
	fmt.Println("9 * 9 = " + fmt.Sprint(num10))
	fmt.Println("10 * 9 = " + fmt.Sprint(num11))
	fmt.Println("11 * 9 = " + fmt.Sprint(num12))
	fmt.Println("12 * 9 = " + fmt.Sprint(num13))
}